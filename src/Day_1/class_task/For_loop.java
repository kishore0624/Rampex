package Day_1.class_task;
public class For_loop {
    public static void main(String[] args) {
        for (int i= 5; i>=1; i--) {
        for(int j=1; j<=i; j++){
                System.out.print(" * ");
            }
            System.out.println();
        }
        System.out.println("Exited from the loop");
    }}
